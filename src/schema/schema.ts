export type User = {
  email: string;
  password: string;
  first_name: string;
  last_name: string;
  pet_information: string;
  user_id: string; // UUID
  order_option: number;
  address: number;
  street: string[];
  city: string[];
  zipcode: string[];
};


export type Order = {
  id: number; // bigint generated by default as identity
  user_id: string; // UUID not null
  cart: number[]; // bigint[] null
  status: number; // bigint null
  order_type: number; // bigint null
  approved: boolean; // boolean null with a default value of false
  pickup_time: number; // bigint null
  items: number; // bigint null
};

export type Schedule = {
  id: number; // bigint generated by default as identity
  date: string; // text not null
  start_time: string; // text null
  end_time: string; // text null
};

export type Product = {
  name: string; // text not null;
  description: string; // text null;
  category: number; // numeric not null;
  quantity: number; // numeric not null;
}
